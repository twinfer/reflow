// Reflw Bootstrap service — kubeadm-style joiner credential exchange.
//
// The bootstrap surface is a single RPC: SignCSR. A joiner that has been
// issued a one-time `reflwd config create-join-token` plaintext sends a
// CertificateSigningRequest plus the token to a cluster member; the
// member verifies the token against shard 0's JoinTokenTable, proposes
// ConsumeJoinToken to atomically mark the row spent, then signs the CSR
// against the cluster CA (the same path the per-node ClusterIssuer uses
// for leaf renewal). The joiner persists the returned leaf to its
// data-dir/certmagic store and proceeds with normal SelfJoin (for node
// kind) or returns the leaf to the operator client (for operator kind).
//
// The bootstrap listener is intentionally segregated from the admin /
// clusterctl listeners. Admin uses RequireAndVerifyClientCert (you need
// a mesh-issued leaf to talk to it); bootstrap accepts unauthenticated
// TLS so a freshly-installed joiner with no leaf yet can reach the
// service. Authentication is *intrinsic to the request* — the join
// token is what authorizes the cert issuance, not a TLS peer cert.
//
// The endpoint is opt-in. Operators set cfg.Bootstrap.Addr to enable
// it; production fleets typically enable it only on a couple of nodes
// (the same nodes that will run `reflwd config create-join-token` for
// their region) and behind a firewall rule that allows freshly-imaged
// hosts to reach the port.

