// ClusterCtlHandler is an implementation of the reflow.clusterctl.v1.ClusterCtl service.
type ClusterCtlHandler interface {
	// AddNode registers a new peer with shard 0 and enqueues a rebalance
	// sequence to give the new node a non-voting replica of every
	// existing partition shard. The non-voting replica is promoted to a
	// voter after dragonboat reports catch-up.
	AddNode(context.Context, *connect.Request[clusterctlv1.AddNodeRequest]) (*connect.Response[clusterctlv1.AddNodeResponse], error)
	// SelfJoin is AddNode initiated by the joiner itself. Authorized when
	// the caller's leaf-CN principal is "node/<req.node_id>"; any other
	// principal is rejected. Same payload and FSM effect as AddNode —
	// the two methods exist as distinct authorization paths so the
	// authz policy can gate them by role (operator/* vs node/<id>).
	// Idempotent: the underlying RegisterNode + BeginRebalanceStep apply
	// arms upsert/dedup.
	SelfJoin(context.Context, *connect.Request[clusterctlv1.AddNodeRequest]) (*connect.Response[clusterctlv1.AddNodeResponse], error)
	// RemoveNode marks a peer as evicted in shard 0. The rebalancer
	// observes and walks the dragonboat membership-change sequence
	// (DELETE_REPLICA for every shard the node hosted).
	RemoveNode(context.Context, *connect.Request[clusterctlv1.RemoveNodeRequest]) (*connect.Response[clusterctlv1.RemoveNodeResponse], error)
	// ListNodes returns the current NodeMembership records from shard 0.
	ListNodes(context.Context, *connect.Request[clusterctlv1.ListNodesRequest]) (*connect.Response[clusterctlv1.ListNodesResponse], error)
	// ListPartitions returns the current PartitionTable from shard 0.
	ListPartitions(context.Context, *connect.Request[clusterctlv1.ListPartitionsRequest]) (*connect.Response[clusterctlv1.ListPartitionsResponse], error)
	// NodeLeadership returns the partition shards this node currently
	// hosts and their live Raft leadership state, read from the node's own
	// runtime view (Host.Partitions) — distinct from ListPartitions, which
	// returns shard 0's assignment table and cannot reflect a live election.
	// Read-only; the chaos/e2e harness uses it to locate a shard's live
	// leader.
	NodeLeadership(context.Context, *connect.Request[clusterctlv1.NodeLeadershipRequest]) (*connect.Response[clusterctlv1.NodeLeadershipResponse], error)
	// CreateSnapshot kicks off an Exported snapshot of the requested
	// partition shard and archives the result via the local
	// SnapshotRepository. Returns the snapshot index.
	CreateSnapshot(context.Context, *connect.Request[clusterctlv1.CreateSnapshotRequest]) (*connect.Response[clusterctlv1.CreateSnapshotResponse], error)
	// ListSnapshots enumerates archived snapshots for a partition shard.
	ListSnapshots(context.Context, *connect.Request[clusterctlv1.ListSnapshotsRequest]) (*connect.Response[clusterctlv1.ListSnapshotsResponse], error)
	// DeleteSnapshot removes a single archived snapshot. Idempotent —
	// returns OK when the snapshot is already absent.
	DeleteSnapshot(context.Context, *connect.Request[clusterctlv1.DeleteSnapshotRequest]) (*connect.Response[clusterctlv1.DeleteSnapshotResponse], error)
	// TransferLP initiates a cross-shard transfer of one logical
	// partition. Validates inputs, mints a UUIDv4 transfer_id, and
	// proposes Command_InitiateLPTransfer to shard 0. The lpMover on the
	// metadata leader then drives the saga (freeze → ship → stage →
	// flip → cleanup). Returns immediately with the transfer_id; the
	// caller polls ListLPTransfers to observe progress. Leader-only.
	TransferLP(context.Context, *connect.Request[clusterctlv1.TransferLPRequest]) (*connect.Response[clusterctlv1.TransferLPResponse], error)
	// ListLPTransfers returns the current LPTransferTable rows plus the
	// table revision. Reads via SyncRead against shard 0.
	ListLPTransfers(context.Context, *connect.Request[clusterctlv1.ListLPTransfersRequest]) (*connect.Response[clusterctlv1.ListLPTransfersResponse], error)
	// RebalanceAdvise returns the autonomous rebalancer's view of the
	// cluster as of the call: current skew, drained shards, per-shard LP
	// counts, and (in `would_transfer`) the moves the rebalancer would
	// propose right now under hysteresis-free evaluation. Read-only —
	// any peer can answer; SyncReads route to the local shard-0 replica.
	RebalanceAdvise(context.Context, *connect.Request[clusterctlv1.RebalanceAdviseRequest]) (*connect.Response[clusterctlv1.RebalanceAdviseResponse], error)
	// RebalanceDrain adds or removes a partition shard from the
	// RebalanceDrainTable. drain=true marks the shard drained (its LPs
	// get re-planned off); drain=false un-drains. Idempotent. CAS via
	// if_table_revision_eq; mismatch returns FailedPrecondition.
	// Leader-only.
	RebalanceDrain(context.Context, *connect.Request[clusterctlv1.RebalanceDrainRequest]) (*connect.Response[clusterctlv1.RebalanceDrainResponse], error)
	// UpsertTenant inserts or replaces one row in shard 0's TenantTable.
	// The server pre-allocates record.id by reading the current
	// TenantList: if a row with record.name already exists, it reuses
	// that id (update path); otherwise it allocates max(existing.id)+1
	// (create path). On create, the assigned id is returned in
	// UpsertTenantResponse.tenant_id. CAS via if_table_revision_eq
	// (caller must pass the revision they read in the same
	// ListTenants call so a racing operator's concurrent edit
	// reproducibly conflicts). Leader-only. Tenants live alongside
	// membership/partitions on the ClusterCtl surface because they are
	// a platform-admin concern; tenant-scoped app config (deployments,
	// event sources, webhooks, secrets) lives on the Config service.
	UpsertTenant(context.Context, *connect.Request[clusterctlv1.UpsertTenantRequest]) (*connect.Response[clusterctlv1.UpsertTenantResponse], error)
	// DeleteTenant removes one row from shard 0's TenantTable by id.
	// Same CAS semantics as UpsertTenant. Delete-of-absent succeeds
	// (and bumps the revision so the operator's CLI sees the proposal
	// landed). Does NOT cascade-delete the tenant's data (invocations,
	// journal entries, DEK record) — operators clean up via a follow-up
	// TenantDEK delete + per-tenant range-delete pass. Leader-only.
	DeleteTenant(context.Context, *connect.Request[clusterctlv1.DeleteTenantRequest]) (*connect.Response[clusterctlv1.DeleteTenantResponse], error)
	// ListTenants returns every TenantRecord plus the table's CAS
	// revision in one SyncRead. Reads from any peer.
	ListTenants(context.Context, *connect.Request[clusterctlv1.ListTenantsRequest]) (*connect.Response[clusterctlv1.ListTenantsResponse], error)
	// DescribeTenant returns one TenantRecord by id, or CodeNotFound.
	// Reads via SyncRead.
	DescribeTenant(context.Context, *connect.Request[clusterctlv1.DescribeTenantRequest]) (*connect.Response[clusterctlv1.DescribeTenantResponse], error)
	// UpsertTenantDEK inserts or replaces one row in shard 0's
	// TenantDEKTable. The DEK plaintext never traverses Raft — the
	// record carries a gocloud.dev/blob URI for the ciphertext and a
	// Tink KMS URI for the KEK that unwraps it. Per-node
	// internal/secretstore.TenantDEKResolver fetches + decrypts at
	// reconcile time and materializes a tink.AEAD primitive cached
	// under record.tenant_id. CAS via if_table_revision_eq. Leader-only.
	// Server rejects record.tenant_id == 0 (the default tenant uses a
	// built-in cluster-wide AEAD, not a TenantDEKTable row).
	UpsertTenantDEK(context.Context, *connect.Request[clusterctlv1.UpsertTenantDEKRequest]) (*connect.Response[clusterctlv1.UpsertTenantDEKResponse], error)
	// DeleteTenantDEK removes one row from shard 0's TenantDEKTable.
	// Same CAS semantics as UpsertTenantDEK. Delete-of-absent succeeds
	// (and bumps the revision). This is the second of the two-step
	// tenant-wipe procedure — running it makes the tenant's data
	// permanently unrecoverable (the DEK is gone). Leader-only.
	DeleteTenantDEK(context.Context, *connect.Request[clusterctlv1.DeleteTenantDEKRequest]) (*connect.Response[clusterctlv1.DeleteTenantDEKResponse], error)
	// ListTenantDEKs returns every TenantDEKRecord plus the table's CAS
	// revision in one SyncRead. Reads from any peer.
	ListTenantDEKs(context.Context, *connect.Request[clusterctlv1.ListTenantDEKsRequest]) (*connect.Response[clusterctlv1.ListTenantDEKsResponse], error)
}

