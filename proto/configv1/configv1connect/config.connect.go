// ConfigHandler is an implementation of the reflow.config.v1.Config service.
type ConfigHandler interface {
	// RegisterDeployment introduces a new handler deployment to the
	// cluster. The engine dials <url>, issues a discovery GET to
	// enumerate handlers, validates the protocol version, mints a
	// UUIDv4 deployment_id, and proposes Command_RegisterDeployment to
	// shard 0. Returns the assigned id. URL must use http:// or
	// https://.
	RegisterDeployment(context.Context, *connect.Request[configv1.RegisterDeploymentRequest]) (*connect.Response[configv1.RegisterDeploymentResponse], error)
	// ListDeployments returns every DeploymentRecord in shard 0 plus the
	// deployment table revision. Reads via SyncRead — any peer can
	// serve. Operators use the returned revision as if_table_revision_eq
	// on subsequent Delete calls.
	ListDeployments(context.Context, *connect.Request[configv1.ListDeploymentsRequest]) (*connect.Response[configv1.ListDeploymentsResponse], error)
	// DescribeDeployment returns one DeploymentRecord by id, or
	// CodeNotFound. Reads via SyncRead.
	DescribeDeployment(context.Context, *connect.Request[configv1.DescribeDeploymentRequest]) (*connect.Response[configv1.DescribeDeploymentResponse], error)
	// DeleteDeployment removes one DeploymentRecord from shard 0 and
	// evicts any (service, handler) → id index entries that pointed to
	// it. Leader-only. force must be true; without it the server returns
	// CodeFailedPrecondition because deleting a deployment is destructive
	// — in-flight invocations resolve their deployment record per
	// stream-open, and a delete breaks any open or replaying invocation
	// pinned to this id. force is the operator's "yes I accept the
	// risk" acknowledgement; reflow does not currently scan partitions
	// for active references.
	DeleteDeployment(context.Context, *connect.Request[configv1.DeleteDeploymentRequest]) (*connect.Response[configv1.DeleteDeploymentResponse], error)
	// UpsertModel / ListModels / DescribeModel / DeleteModel manage shard 0's
	// ModelTable — BPMN/CMMN model definitions (model_ref + inlined XML). Upsert
	// parses-to-validate before proposing Command_UpsertModel; the per-node
	// iflowengine TableResolver reconciles rows into parsed graphs + resolved
	// historyTimeToLive. Leader-only for mutating calls; List/Describe are
	// SyncRead. DeleteModel takes if_table_revision_eq for CAS (no force gate —
	// an in-flight instance pins its model_ref and fails on its next turn).
	UpsertModel(context.Context, *connect.Request[configv1.UpsertModelRequest]) (*connect.Response[configv1.UpsertModelResponse], error)
	ListModels(context.Context, *connect.Request[configv1.ListModelsRequest]) (*connect.Response[configv1.ListModelsResponse], error)
	DescribeModel(context.Context, *connect.Request[configv1.DescribeModelRequest]) (*connect.Response[configv1.DescribeModelResponse], error)
	DeleteModel(context.Context, *connect.Request[configv1.DeleteModelRequest]) (*connect.Response[configv1.DeleteModelResponse], error)
	// UpsertSecret / DeleteSecret / ListSecrets mirror the same trio
	// against shard 0's SecretTable. Each SecretRecord references a
	// ciphertext blob (gocloud.dev/blob URI) and a KEK (Tink KMS URI);
	// the plaintext never traverses Raft. Per-node
	// internal/secretstore Reconcilers fetch + decrypt at reconcile
	// time and surface bytes via in-memory Lookup. Leader-only for
	// mutating calls; List is SyncRead.
	UpsertSecret(context.Context, *connect.Request[configv1.UpsertSecretRequest]) (*connect.Response[configv1.UpsertSecretResponse], error)
	DeleteSecret(context.Context, *connect.Request[configv1.DeleteSecretRequest]) (*connect.Response[configv1.DeleteSecretResponse], error)
	ListSecrets(context.Context, *connect.Request[configv1.ListSecretsRequest]) (*connect.Response[configv1.ListSecretsResponse], error)
	// UpsertCARoot / DeleteCARoot / ListCARoots mirror the secret trio
	// against shard 0's CARootTable. Each CARootRecord carries a CA
	// cert (PEM) plus a pointer to the SecretTable row holding the
	// AEAD-wrapped signing key. The signing key never traverses Raft.
	// Per-node certmgr.ClusterIssuer fetches + decrypts via
	// secretstore.LookupForCASigning at reconcile time. Leader-only for
	// mutating calls; List is SyncRead.
	UpsertCARoot(context.Context, *connect.Request[configv1.UpsertCARootRequest]) (*connect.Response[configv1.UpsertCARootResponse], error)
	DeleteCARoot(context.Context, *connect.Request[configv1.DeleteCARootRequest]) (*connect.Response[configv1.DeleteCARootResponse], error)
	ListCARoots(context.Context, *connect.Request[configv1.ListCARootsRequest]) (*connect.Response[configv1.ListCARootsResponse], error)
	// CreateJoinToken mints a one-time kubeadm-style joiner credential.
	// The server generates a random plaintext, persists only its sha256
	// hash + a JoinTokenRecord into shard 0's JoinTokenTable, and
	// returns the plaintext to the operator exactly once. Subsequent
	// List calls show only the hash. Operators redeem the plaintext via
	// `reflowd run --join`. Leader-only.
	CreateJoinToken(context.Context, *connect.Request[configv1.CreateJoinTokenRequest]) (*connect.Response[configv1.CreateJoinTokenResponse], error)
	// DeleteJoinToken removes a row by hex(token_hash) — surfaces in
	// ListJoinTokens. Idempotent.
	DeleteJoinToken(context.Context, *connect.Request[configv1.DeleteJoinTokenRequest]) (*connect.Response[configv1.DeleteJoinTokenResponse], error)
	// ListJoinTokens returns every JoinTokenRecord. SyncRead.
	ListJoinTokens(context.Context, *connect.Request[configv1.ListJoinTokensRequest]) (*connect.Response[configv1.ListJoinTokensResponse], error)
	// IssueOperator mints an operator client cert against the active
	// cluster CA. The operator generates the keypair locally, sends the
	// CSR, and receives the signed leaf + CA chain. Authorization is via
	// the existing operator/* policy gate; the in-server signer is the
	// same ClusterIssuer the bootstrap listener uses. Leader-only.
	// Replaces the deleted `reflowd pki issue-operator` flow.
	IssueOperator(context.Context, *connect.Request[configv1.IssueOperatorRequest]) (*connect.Response[configv1.IssueOperatorResponse], error)
	// IssueTenant mints a tenant client cert (CN "tenant/<n>", n in [1,255])
	// against the active cluster CA — the source identity for LP-band tenancy.
	// Same flow as IssueOperator (operator generates the keypair, sends the
	// CSR, receives the signed leaf + CA chain); the server validates the CN's
	// numeric tenant band and signs the public key. Operator-only, leader-only.
	IssueTenant(context.Context, *connect.Request[configv1.IssueTenantRequest]) (*connect.Response[configv1.IssueTenantResponse], error)
	// UpsertClusterAuthzPolicy replaces shard 0's cluster-wide Cedar authz
	// policy text (PlatformConfigRecord). The server runs layer-1 schema
	// validation before proposing, so an invalid policy is rejected at upload,
	// never installed. CAS via if_table_revision_eq. Operator-only. Leader-only.
	UpsertClusterAuthzPolicy(context.Context, *connect.Request[configv1.UpsertClusterAuthzPolicyRequest]) (*connect.Response[configv1.UpsertClusterAuthzPolicyResponse], error)
	// GetClusterAuthzPolicy returns the current cluster authz policy text + the
	// platform-config table revision (for a CAS round-trip). Operator-only.
	GetClusterAuthzPolicy(context.Context, *connect.Request[configv1.GetClusterAuthzPolicyRequest]) (*connect.Response[configv1.GetClusterAuthzPolicyResponse], error)
}

