// Reflw inter-node Delivery service.
//
// Every reflwd process hosts a Delivery gRPC server. When a shard's
// leader-side OutboxService sees an OutboxEnvelope whose
// destination_shard_id does not match the local shard, it dials the
// destination shard's current leader via this service and streams the
// envelope as a Command. The receiver's Delivery server calls the local
// RaftProposer.ProposeIngress on the destination shard if and only if
// this node holds that shard's leadership.
//
// A successful Ack means "I have durably accepted responsibility for
// proposing this command via Raft" — durable apply on the receiver
// triggers a reciprocal OutboxAck envelope that pops the sender's
// outbox row. NotLeader carries the receiver's leader hint so the
// sender can re-resolve via the dragonboat gossip ShardView and retry.
//
